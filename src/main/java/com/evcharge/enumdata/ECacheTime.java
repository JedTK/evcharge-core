package com.evcharge.enumdata;

/**
 * 常用的缓存时间（ms级别）
 */
public class ECacheTime {
    /**
     * 一年，365天
     */
    public final static long YEAR = 86400000L * 365;
    /**
     * 一个月，30天
     */
    public final static long MONTH = 86400000L * 30;
    /**
     * 一周，7日
     */
    public final static long WEEK = 86400000L * 7;
    /**
     * 一日
     */
    public final static long DAY = 86400000L;

    /**
     * 1小时
     */
    public final static long HOUR = 3600000L;
    /**
     * 1分钟
     */
    public final static long MINUTE = 60000L;
    /**
     * 1秒钟
     */
    public final static long SECOND = 1000L;
}
